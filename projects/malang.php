            <h1>1. Goa Pinus</h1>
            <img src="../assets/images/goapinus.jpg" alt="Goa Pinus">
            <p>
                Nestled amidst the lush greenery of Malang, East Java, Goa Pinus offers a tranquil escape into nature's embrace. This enchanting pine forest, with its towering trees and winding trails, invites visitors to unwind and reconnect with the natural world.
                <br><br>
                As you venture deeper into the forest, you'll be greeted by the crisp, clean air and the soothing sounds of nature. The towering pine trees create a canopy of green, filtering the sunlight and casting dappled shadows on the forest floor. The tranquility of the forest is only broken by the occasional chirping of birds and the rustling of leaves.
                <br><br>
                Goa Pinus offers a variety of activities for visitors of all ages. Hiking enthusiasts can explore the well-maintained trails, ranging from easy to challenging. For those seeking a more leisurely experience, there are picnic areas and camping sites where you can relax and enjoy the serene atmosphere.
            </p>
           

            <h2>2. Kebun Teh</h2>
            <img src="../assets/images/kebunteh.jpg" alt="Kebun Teh">
            <p>
                Kebun Teh, or Tea Plantation, is a captivating destination in Bandung, West Java. This sprawling tea plantation offers breathtaking views of rolling hills covered in lush green tea bushes. As you wander through the plantation, you'll be greeted by the fragrant aroma of freshly brewed tea and the serene ambiance of the surrounding nature.
                <br><br>
                Visitors can take guided tours of the plantation to learn about the tea-making process, from harvesting to processing. You can also indulge in a cup of freshly brewed tea while admiring the stunning views.
                <br><br>
                Beyond the tea plantations, Kebun Teh offers a variety of outdoor activities, such as hiking, cycling, and camping. The cool, crisp air and the picturesque scenery make it a perfect escape from the hustle and bustle of city life.
            </p>
          
            <h2>3. Pantai Tiga Warna</h2>
            <img src="../assets/images/pantaitigawarna.jpg" alt="Pantai Tiga Warna">
            <p>
                Pantai Tiga Warna, or Three-Colored Beach, is a stunning coastal destination located in Raja Ampat, West Papua. This hidden gem is renowned for its crystal-clear waters and unique three-colored sea, which is caused by the reflection of light on the seabed.
                <br><br>
                The beach offers a variety of water activities, including snorkeling, diving, and kayaking. Snorkelers and divers can explore the vibrant coral reefs and encounter a diverse array of marine life. The crystal-clear waters provide excellent visibility, allowing you to admire the underwater beauty.
                <br><br>
                For those who prefer to relax, the beach offers pristine white sand and swaying palm trees. You can simply unwind on the beach, soak up the sun, and listen to the soothing sound of the waves.
            </p>
           
