            <h1>1. Taman Tabebuya</h1>
            <img src="../assets/images/tamantabebuya.jpg" alt="Taman Tabebuya">
            <p>
                Taman Tabebuya, a serene urban oasis, is a popular destination in Jakarta, particularly during the blooming season. 
                This park is renowned for its stunning display of yellow Tabebuya trees, which create a breathtaking spectacle. As visitors stroll along the winding paths, they are greeted by a vibrant tapestry of yellow blooms, offering a tranquil escape from the bustling city. 
                <br><br>
                The park also provides various facilities, including playgrounds for children, fitness equipment, and ample seating areas, making it an ideal spot for families and individuals to relax and unwind. 
                Beyond its aesthetic appeal, Taman Tabebuya offers a much-needed respite from the hustle and bustle of city life, providing a peaceful environment to connect with nature.
            </p>

            <h2>2. Ancol Dreamland</h2>
            <img src="../assets/images/ancol.jpg" alt="Ancol Dreamland">
            <p>
                Ancol Dreamland is a popular theme park in Jakarta, offering a wide range of exciting attractions for visitors of all ages. This sprawling entertainment complex boasts a diverse array of activities, from thrilling rides to relaxing water parks, ensuring a fun-filled day for the entire family.
                <br><br>
                Thrill-seekers can experience the adrenaline rush of roller coasters, while water enthusiasts can cool off in the refreshing waters of the water park. For those seeking a more leisurely experience, the park offers a variety of cultural shows, live performances, and shopping opportunities.
                <br><br>
                Ancol Dreamland is more than just a theme park; it is a destination that offers a unique blend of entertainment, education, and relaxation.
            </p>
            <h3>3. Kota Tua Jakarta</h3>
            <img src="../assets/images/kotatuajakarta.jpg" alt="Kota Tua Jakarta">
            <p>
                Kota Tua, also known as Old Batavia, is a historic district in Jakarta that offers a glimpse into the city's colonial past. This charming neighborhood is filled with beautifully preserved colonial buildings, cobblestone streets, and museums, transporting visitors back in time.
                <br><br>
                Visitors can explore the iconic Fatahillah Square, a bustling plaza surrounded by historic buildings, and admire the stunning architecture of the Jakarta History Museum. The district also hosts various cultural events and festivals throughout the year, showcasing the rich cultural heritage of Jakarta.
                <br><br>
                Kota Tua is a must-visit destination for history buffs and anyone interested in experiencing Jakarta's rich cultural heritage.
            </p>
