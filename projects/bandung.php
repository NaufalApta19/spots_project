            <h1>1. Bukit Moko</h1>
            <img src="../assets/images/bukitmoko.jpg" alt="Bukit Moko">
            <p>
                Perched atop a hill at an altitude of approximately 1,500 meters above sea level, Bukit Moko offers a breathtaking panoramic view of Bandung City. Surrounded by lush pine forests, this serene destination is a haven for nature lovers and photography enthusiasts. As visitors ascend the hill, they are greeted by increasingly stunning vistas of the city skyline and the surrounding mountains.
                <br><br>
                The early morning and late afternoon are particularly magical times to visit Bukit Moko, as the golden hues of sunrise and sunset cast a warm glow over the landscape. The cool, crisp air and the tranquility of the surrounding nature make it a perfect escape from the bustling city.
            </p>
         
            
            <h2>2. Kampung Cairancaupas</h2>
            <img src="../assets/images/kampungcairancaupas.jpg" alt="Kampung Cairancau">
            <p>
                Nestled amidst the picturesque landscapes of West Java, Kampung Cai Rancaupas offers a glimpse into traditional rural life. Surrounded by lush tea plantations and serene rice fields, this charming village provides a peaceful retreat from the urban hustle and bustle.
                <br><br>
                Visitors can immerse themselves in the slow pace of village life, interact with friendly locals, and experience firsthand the simple pleasures of rural living. The village offers a variety of activities, including hiking, biking, and traditional crafts workshops. For those seeking a truly authentic experience, homestays provide an opportunity to stay overnight and experience the warmth of local hospitality.
            </p>
          

            <h2>3. Tebing Keraton</h2>
            <img src="../assets/images/tebingkeraton.jpg" alt="Tebing Keraton">
            <p>
                For those seeking an adventure, Tebing Keraton offers a challenging hike with breathtaking rewards. This popular hiking destination is located within the Djuanda Forest Park and requires a moderate level of fitness.
                <br><br>
                The trail winds through lush forests, offering glimpses of stunning natural beauty. As hikers ascend the steep slopes, they are rewarded with panoramic views of the city and the surrounding mountains. The summit offers a breathtaking vista, making the challenging hike worthwhile.
                <br><br>
                It's important to note that the trail can be slippery, especially during rainy seasons, so proper footwear and caution are advised.
            </p>
          
