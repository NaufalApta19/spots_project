            <h1>1. Kampung Pelangi</h1>
            <img src="../assets/images/kampungpelangi.jpg" alt="Kampung Pelangi">
            <p>
                Kampung Pelangi, once a humble neighborhood, has transformed into a vibrant and colorful tourist destination. The walls of houses, streets, and even alleyways are adorned with a kaleidoscope of colors, creating a cheerful and Instagram-worthy atmosphere. Every corner offers unique photo opportunities, making it a paradise for photography enthusiasts.
                <br><br>
                Beyond its aesthetic appeal, Kampung Pelangi provides a glimpse into the daily lives of local residents and the diversity of Indonesian culture. Visitors can explore the neighborhood, admire the colorful murals, and immerse themselves in the lively atmosphere.
            </p>
            
            <h2>2. Lawang Sewu</h2>
            <img src="../assets/images/lawangsewu.jpg" alt="Lawang Sewu">
            <p>
                Lawang Sewu, a historic building with a thousand doors, is shrouded in mystery and intrigue. This colonial-era building has witnessed the passage of time and bears the scars of history. Its unique architecture and eerie atmosphere have made it a popular destination for history buffs and thrill-seekers alike.
                <br><br>
                Visitors can explore the dark corridors, descend winding staircases, and feel the weight of history within its walls. Lawang Sewu has also gained notoriety as a location for horror films, adding to its mysterious allure.
            </p>

            <h2>3. Sam Poo Kong</h2>
            <img src="../assets/images/sampokong.jpeg" alt="Sam Poo Kong">
            <p>
                Sam Poo Kong, or Klenteng Sam Poo Kong, is a unique blend of Chinese and Javanese cultures. This temple complex was built to honor Admiral Zheng He, a Chinese explorer who played a significant role in spreading Islam in Southeast Asia.
                <br><br>
                The temple's beautiful architecture and ancient artifacts offer a glimpse into the rich history and cultural diversity of Indonesia. Visitors can witness various religious rituals performed by the Chinese community and experience the serene atmosphere of the temple grounds.
            </p>
